package com.example.item;

public class YtChannelItem {

    private String ChannelId;
    private String ChannelName;
    private String ChannelUserName;
    private String ChannelImageUrl;

    public String getChannelId() {
        return ChannelId;
    }

    public void setChannelId(String Playlistid) {
        this.ChannelId = Playlistid;
    }


    public String getChannelName() {
        return ChannelName;
    }

    public void setChannelName(String Playlistname) {
        this.ChannelName = Playlistname;
    }

    public String getChannelImageurl() {
        return ChannelImageUrl;
    }

    public void setChannelImageurl(String Playlistimage) {
        this.ChannelImageUrl = Playlistimage;
    }

    public String getChannelUserName() {
        return ChannelUserName;
    }

    public void setChannelUserName(String Playlistusername) {
        this.ChannelUserName = Playlistusername;
    }

}
